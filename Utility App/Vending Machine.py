# Initialize colorama for cross-platform compatibility
init(autoreset=True)

# Vending machine items and their prices
items = [
    ["1", "Chips", "$1.50"],
    ["2", "Soda", "$1.25"],
    ["3", "Candy", "$1.00"],
    ["4", "Chocolate", "$1.75"],
    ["5", "Water", "$1.00"],
    ["6", "Gum", "$0.50"],
]
prices = {
    "1": 1.50,
    "2": 1.25,
    "3": 1.00,
    "4": 1.75,
    "5": 1.00,
    "6": 0.50,
}


# Display the vending machine
def display_vending_machine():
    clear_screen()
    print(Fore.RED + Style.BRIGHT + "=========================")
    print("       VENDING MACHINE       ")
    print("=========================" + Style.RESET_ALL)
    print(tabulate(items, headers=["Code", "Item", "Price"], tablefmt="fancy_grid"))
    print("=========================")

# Main program
def vending_machine():
    total_cost = 0.0
    selected_items = []

    while True:
        display_vending_machine()
        print(f"Total Cost: ${total_cost:.2f}")
        print(f"Selected Items: {', '.join(selected_items) if selected_items else 'None'}")
        print("\nEnter the item code to purchase or type 'done' to end.")

        user_input = input("Your choice: ").strip()

        if user_input.lower() == "done":
            break

        if user_input in prices:
            item_name = next(item[1] for item in items if item[0] == user_input)
            item_price = prices[user_input]
            selected_items.append(item_name)
            total_cost += item_price
            print(f"\n{item_name} added to your cart. (${item_price:.2f})")
        else:
            print("\nItem not availabe. Please try something else.")

        input("\nPress Enter to carry on.....")

    clear_screen()
    print(Fore.GREEN + "Thank you for using the vending machine!")
    print(f"Your final amount is: ${total_cost:.2f}")
    print(f"Items purchased: {', '.join(selected_items) if selected_items else 'None'}")

# Run the vending machine program
vending_machine()
